    CONSTRAINT uc_curricula_vitaes_jobseeker UNIQUE (jobseeker_id)
);

CREATE TABLE public.educations
(
    id                 INTEGER NOT NULL GENERATED ALWAYS AS IDENTITY ( INCREMENT 1 START 1 MINVALUE 1 MAXVALUE 999999 CACHE 1 ),
